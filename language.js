function redirectResultLinksToPassport() {
  const params = new URLSearchParams(window.location.search);
  if (!params.has("result")) return;
  if (window.location.pathname.endsWith("/passport.html") || window.location.pathname.endsWith("passport.html")) return;

  const url = new URL("./passport.html", window.location.href);
  url.search = window.location.search;
  url.hash = window.location.hash || "#test";
  window.location.replace(url.toString());
}

