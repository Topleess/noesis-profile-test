const textNodeOriginals = new WeakMap();
const translatedAttributes = ["placeholder", "aria-label", "title"];
let activeLanguage = fallbackLanguage;
let isApplyingLanguage = false;

function normalizeLanguage(value) {
  const code = String(value || "").toLowerCase().slice(0, 2);
  return supportedLanguages.includes(code) ? code : null;
}

function normalizeText(value) {
  return String(value || "").replace(/\s+/g, " ").trim();
}

function translate(original, language) {
  if (language === fallbackLanguage) return original;
  return translations[language]?.[original] || original;
}

function translateValue(value, language = activeLanguage) {
  const original = normalizeText(value);
  return original ? translate(original, language) : "";
}

function getInitialLanguage() {
  const params = new URLSearchParams(window.location.search);
  return (
    normalizeLanguage(params.get("lang")) ||
    normalizeLanguage(window.localStorage.getItem("whoami_language")) ||
    normalizeLanguage(navigator.language) ||
    fallbackLanguage
  );
}

function withLanguageParam(href, language) {
  try {
    const url = new URL(href, window.location.href);
    if (url.origin !== window.location.origin) return href;
    if (!url.pathname.endsWith(".html") && url.pathname !== "/" && url.pathname !== window.location.pathname) return href;
    url.searchParams.set("lang", language);
    if (url.protocol === "file:") return url.href;
    return `${url.pathname}${url.search}${url.hash}`;
  } catch {
    return href;
  }
}

function translateTextNode(node, language) {
  const current = node.textContent;
  const normalized = normalizeText(current);
  if (!normalized) return;

  if (!textNodeOriginals.has(node)) {
    textNodeOriginals.set(node, normalized);
  }

  const original = textNodeOriginals.get(node);
  const translated = translate(original, language);
  const leading = current.match(/^\s*/)?.[0] || "";
  const trailing = current.match(/\s*$/)?.[0] || "";
  node.textContent = `${leading}${translated}${trailing}`;
}

function translateAttributes(root, language) {
  const targets = root.querySelectorAll ? [root, ...root.querySelectorAll("*")] : [];

  targets.forEach((element) => {
    translatedAttributes.forEach((attribute) => {
      if (!element.hasAttribute?.(attribute)) return;
      const key = `i18nOriginal${attribute.replace(/(^|-)([a-z])/g, (_, __, letter) => letter.toUpperCase())}`;
      const current = normalizeText(element.getAttribute(attribute));
      if (!current) return;
      if (!element.dataset[key]) element.dataset[key] = current;
      element.setAttribute(attribute, translate(element.dataset[key], language));
    });
  });
}

function translateTextNodes(root, language) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!normalizeText(node.textContent)) return NodeFilter.FILTER_REJECT;
      if (node.parentElement?.closest("script, style, textarea, [data-language-switcher]")) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => translateTextNode(node, language));
}

function translateDocumentMeta(language) {
  if (!document.documentElement.dataset.i18nOriginalTitle) {
    document.documentElement.dataset.i18nOriginalTitle = document.title;
  }
  document.title = translate(document.documentElement.dataset.i18nOriginalTitle, language);

  document.querySelectorAll('meta[name="description"]').forEach((meta) => {
    if (!meta.dataset.i18nOriginalContent) meta.dataset.i18nOriginalContent = meta.getAttribute("content") || "";
    meta.setAttribute("content", translate(meta.dataset.i18nOriginalContent, language));
  });
}

function updateLanguageSwitcher() {
  document.querySelectorAll("[data-current-language]").forEach((element) => {
    element.textContent = languageShortLabels[activeLanguage] || languageShortLabels[fallbackLanguage];
  });
}

function applyTranslations(root = document.body) {
  if (!root) return;
  isApplyingLanguage = true;
  translateTextNodes(root, activeLanguage);
  translateAttributes(root, activeLanguage);
  translateDocumentMeta(activeLanguage);
  isApplyingLanguage = false;
}

function applyLanguage(language) {
  activeLanguage = normalizeLanguage(language) || fallbackLanguage;

  document.documentElement.lang = activeLanguage;
  document.documentElement.dir = activeLanguage === "ar" ? "rtl" : "ltr";
  window.localStorage.setItem("whoami_language", activeLanguage);

  document.querySelectorAll("[data-lang-option]").forEach((button) => {
    const isActive = button.dataset.langOption === activeLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  document.querySelectorAll("a[href]").forEach((link) => {
    const href = link.getAttribute("href");
    if (!href || href.startsWith("#") || href.startsWith("mailto:") || href.startsWith("tel:")) return;
    link.setAttribute("href", withLanguageParam(href, activeLanguage));
  });

  applyTranslations(document.body);
  updateLanguageSwitcher();
  window.dispatchEvent(new CustomEvent("whoami:languagechange", { detail: { language: activeLanguage } }));
}

document.querySelectorAll("[data-lang-option]").forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.langOption);
    const switcher = button.closest("[data-language-switcher]");
    if (switcher && "open" in switcher) switcher.open = false;
  });
});

document.addEventListener("click", (event) => {
  document.querySelectorAll("[data-language-switcher][open]").forEach((switcher) => {
    if (!switcher.contains(event.target)) switcher.open = false;
  });
});

document.addEventListener("keydown", (event) => {
  if (event.key !== "Escape") return;
  document.querySelectorAll("[data-language-switcher][open]").forEach((switcher) => {
    switcher.open = false;
  });
});

const translationObserver = new MutationObserver((mutations) => {
  if (isApplyingLanguage || activeLanguage === fallbackLanguage) return;
  window.requestAnimationFrame(() => {
    mutations.forEach((mutation) => {
      mutation.addedNodes.forEach((node) => {
        if (node.nodeType === Node.ELEMENT_NODE || node.nodeType === Node.TEXT_NODE) {
          applyTranslations(node.nodeType === Node.TEXT_NODE ? node.parentElement : node);
        }
      });
    });
  });
});

if (document.body) {
  translationObserver.observe(document.body, { childList: true, subtree: true });
} else {
  window.addEventListener(
    "DOMContentLoaded",
    () => translationObserver.observe(document.body, { childList: true, subtree: true }),
    { once: true }
  );
}

window.whoamiApplyLanguage = applyLanguage;
window.whoamiTranslate = applyTranslations;
window.whoamiT = translateValue;

applyLanguage(getInitialLanguage());
